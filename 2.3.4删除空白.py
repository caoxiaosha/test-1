
# 删除右边的空格
message = "你好,河小象   "
print(message)
message = "你好,河小象   ".rstrip()
print(message)

# 删除左边的空格
message = "    你好,小景   "
print(message)
message = "    你好,小景   ".lstrip()
print(message)

# 删除两边的空格
message = "    你好,    琪琪   "
print(message)
message = "    你好,    琪琪   ".strip()
print(message)
